                <h1>Playstation 3</h1>
                <p class="lead">Sommaren 2023 köpte jag en Playstation 3 Superslim 300GB. Den kostade bara några hundralappar, var i jättefint skick och kom med några spel.</p>
<img src="ps3.jpg" align="right" hspace="10" vspace="10" class="img-fluid"/>
<p>Jag köpte på mig lite fler spel och tänkte jag skulle dela med mig av mina tankar om dem här. De är ju förstås gamla vid det här laget så det är ingen idé att jämföra dem med de senaste spelen. Men även om de inte är det senaste kan de fortfarande vara underhållande.</p>

<p>Överlag då, hur funkar det med PS3? Faktiskt riktigt bra. Den ansluter till internet med wifi och det fanns en uppdatering till min (det var firmwareuppdateringen som Sony släppte i början av 2023 tror jag). Jag spelar väldigt sällan onlinspel så den aspekter kommer jag inte nämna eller prova.</p>

<p>Vad jag vet görs inga nya spel för PS3 längre, men begagnatmarknaden är stor. För egen del fungerar Tradera bäst, men jag har köpt lite på Blocket också. Det går att göra bra klipp, några spel har jag köpt för en krona (plus frakt). Vissa spel kostar lite mer, men för några hundralappar kan du köpa nästan vilket spel du vill.</p>

<p><b>Call of duty 3:</b> Ett av mina favoritspel. Snygg grafik, bra kontroller, schysst story.</p>

<p><b>Elder Scrolls Skyrim:</b> Denna typ av spel blir sällan några favoriter för mig, men Skyrim har jag spelat mycket. Står sig bra än i dag.</p>

<p><b>Medal of honor Airborne:</b> Den här har jag spelat jättemycket och återkommer till även på PC. Känns modern trots sin ålder.</p>

<p><b>Killzone 2 ock 3:</b> Bland de bästa skjutaspelen någonsin, håller än i dag.</p>

<p><b>The last of us:</b> Rankas av många som ett av de bästa PS3-spelen någonsin och det är bara att hålla med. Kanske inte ett spel man återkommer till och spelar igen och igen, men även om det bara blir en gång är det en väldigt bra upplevelse.</p>

<p><b>Little Big Planet 2:</b> Kanske ett spel mest för barn, men här hemma apelar vi vuxna, ofta tillsammans faktiskt för det går att vara två spelare samtidigt. Charmigt, gulligt, snygg grafik och utmanande spel. En vinnare!</p>

<p><b>Wolfenstein och Wolfenstein The New Order:</b> Ja, vad ska man säga. Det är action, snygg grafik, och pulshöjande. Och en hel del nostalgi. Båda spelen håller än.</p>

<p><b>Uncharted 2 och 3:</b> Här är det äventyr à la Indiana Jones kombinerat med hoppa och klättra som i Assasins Creed. Underhållningsvärdet är stort i båda spelen. Välj trean om du ska välja bara ett av dem, annars är båda väl värda ett köp.</p>

<p><b>Bioshock 2 och Infinite:</b> Skjutaspel, ja, men så mycket mer. Jag föredrar militära skjutaspel men här är miljön och storyn så fångande att man häpnar. Köp bara!</p>

<p><b>Red Dead Redemption:</b> Vilda västern, öppen värld, rida och jaga skurkar, lösa pussel. Tummen upp!</p>

<p><b>GTA 4 och 5:</b> Inte riktigt mina favoritspel, men man måste ju prova. Femman är klart bättre och bjuder på väldigt många timmar underhållning.</p>

<p><b>Far Cry 3 och 4:</b> Far Cry (alla versioner) har jag spelat mest på PC, men de står sig bra även på PS3.</p>

<p><b>Mass Effect 2:</b> Skjutaspel i rymdmiljö. En häftig upplevelse, rekommenderas. Finns en version tre också som jag inte har provat.</p>

<p><b>Journey:</b> Ett av de mest udda spelen någonsin, och vackert som tusan i sin enkelhet. Jag spelar det sällan, men min fru tycker om det.</p>

<p><b>Assasins Creed:</b> Här finns en uppsjö versioner att välja mellan, med stora likheter i hur de spelas, men som utspelar sig i olika tider och platser. Än så länge har jag provat originalet och tvåan. Spelmekanik som gjord för kontroller snarare än tangentbord, bra story och snygg grafik. Inte spel jag återkommer till så mycket men mycket underhållning i alla fall.</p>

<p><b>NFL/NHL/Madden:</b> Sportspel är inte min grej, men de är billiga och finns i stor upplaga på begagnatmarknaden. Hemma hos oss spelar mest min fru. Årgång spelar inte så stor roll, tycker jag. Men grafiken blir lite bättre med nyare årgångar och en del funktionalitet tillkommer. Men räkna inte med aktuella lag och spelare.</p>

